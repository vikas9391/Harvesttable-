interface LangCtxType {
  lang: LangCode
  setLang: (code: LangCode) => void
  t: (key: string, vars?: Record<string, string>) => string
  isRTL: boolean
}

const LangContext = createContext<LangCtxType>({
  lang: 'en', setLang: () => {}, t: (k) => k, isRTL: false,
})

export const useLanguage = () => useContext(LangContext)

export const LangProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [lang, setLangState] = useState<LangCode>(() =>
    (localStorage.getItem('ht_lang') as LangCode) || 'en'
  )

  const setLang = (code: LangCode) => {
    setLangState(code)
    localStorage.setItem('ht_lang', code)
  }

  const isRTL = lang === 'ar'

  useEffect(() => {
    document.documentElement.lang = lang
    document.documentElement.dir = isRTL ? 'rtl' : 'ltr'
  }, [lang, isRTL])

  const t = (key: string, vars?: Record<string, string>): string => {
    let str = translations[lang][key] ?? translations['en'][key] ?? key
    if (vars) Object.entries(vars).forEach(([k, v]) => { str = str.replace(`{{${k}}}`, v) })
    return str
  }

  return (
    <LangContext.Provider value={{ lang, setLang, t, isRTL }}>
      {children}
    </LangContext.Provider>
  )
}